#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;
	int c;

	i = 0;
	while ((c = str[i]) != '\0')
	{
		ft_putchar(c);
		i++;
	}
}
